function showPan() {
	var c = document.getElementById("weiqi");
	var cxt = c.getContext("2d");
	for (var i = 0; i < 19; i++) {
		for (var j = 0; j < 19; j++) {
			if (pan[i][j] === 1) { //black
				cxt.beginPath();
				cxt.arc((i+1)*30, (j+1)*30,15,0,2*Math.PI,false);
				cxt.fillStyle="black";
				cxt.fill();
			}
			else if (pan[i][j] === 2) { //white
				cxt.beginPath();
				cxt.arc((i+1)*30, (j+1)*30,15,0,2*Math.PI,false);
				cxt.fillStyle="white";
				cxt.fill();
			}
			else {
				/*
				cxt.beginPath();
				cxt.arc((i+1)*30, (j+1)*30,15,0,2*Math.PI,false);
				cxt.fillStyle="gray";
				cxt.fill();
				*/
			}
		}
	}
}

addLoadEvent(showPan);
